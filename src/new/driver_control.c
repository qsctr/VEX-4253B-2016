#define JOYSTICK_DRIVE vexRT[Ch3]
#define JOYSTICK_STRAFE vexRT[Ch4]
#define JOYSTICK_ROTATE vexRT[Ch1]

static void dc_base(void);

task usercontrol()
{
    dc_base();
    sleep(20);
}

static void dc_base()
{
    if (JOYSTICK_ROTATE) {
        base_rotate(JOYSTICK_ROTATE);
    } else {
        base_main_set(JOYSTICK_DRIVE);
        base_strafe_set(JOYSTICK_STRAFE);
    }
}
